CREATE TABLE eg_ind_registration(
  id VARCHAR(255) PRIMARY KEY,
  individualId VARCHAR(255),
  tenantId VARCHAR(255),
  clientReferenceId VARCHAR(255),
  user_id VARCHAR(255),
  givenName VARCHAR(255),
  familyName VARCHAR(255),
  otherNames VARCHAR(255),
  dateOfBirth VARCHAR(255),
  gender VARCHAR(255),
  bloodGroup VARCHAR(255),
  mobileNumber VARCHAR(255),
  altContactNumber VARCHAR(255),
  email VARCHAR(255),
  fatherName VARCHAR(255),
  husbandName VARCHAR(255),
  relationship VARCHAR(255),
  photo text,
  additionalFields JSONB,
  is_deleted VARCHAR(255),
  is_SystemUser VARCHAR(255),
  rowVersion bigint,
  createdBy VARCHAR(255),
  lastModifiedBy VARCHAR(255),
  createdTime bigint,
  lastModifiedTime bigint
);
